using ll = long long;

ll sum(ll x){
    ll e = x, o = x - 1;
    if(e & 1) swap(e, o);
    o = ((o + 1) / 2) * (o + 1) / 2;
    e = (e / 2) * (e + 2) / 2;
    return e - o;
}

void solve(){
    ll l, r;
    cin >> l >> r;
    cout << sum(r) - sum(l - 1) << '\n';
}

int32_t main() {
    cin.tie(nullptr)->sync_with_stdio(0);
    int test = 1;
    cin >> test;
    for(int i = 1; i <= test; ++i){
        solve();
    }
    cerr << "\nTime elapsed: " << 1000.0 * clock() / CLOCKS_PER_SEC << " ms.\n";
    return 0;
}